package encap;

public class Patients {

    private String name;
    private int age;
    private int pat_no;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getPat_no() {
        return pat_no;
    }

    public void setPat_no(int pat_no) {
        this.pat_no = pat_no;
    }
}
